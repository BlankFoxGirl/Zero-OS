static constexpr int FONT_FIRST = 32;
static constexpr int FONT_LAST  = 126;

static struct {
    uint8_t *fb;
    uint32_t pitch;
    uint32_t width;
    uint32_t height;
    uint8_t  bpp;
    uint32_t cx, cy;
    uint32_t cols, rows;
    bool     ready;
} con;

static constexpr uint32_t FG = 0x00CCCCCC;  // light grey
static constexpr uint32_t BG = 0x00000000;  // black

static void plot(uint32_t x, uint32_t y, uint32_t color) {
    uint32_t bytes_pp = con.bpp / 8;
    uint8_t *px = con.fb + y * con.pitch + x * bytes_pp;
    if (con.bpp == 32) {
        *reinterpret_cast<uint32_t *>(px) = color;
    } else if (con.bpp == 24) {
        px[0] = color & 0xFF;
        px[1] = (color >> 8) & 0xFF;
        px[2] = (color >> 16) & 0xFF;
    } else if (con.bpp == 16) {
        uint16_t c16 = static_cast<uint16_t>(
            ((color >> 8) & 0xF800) |
            ((color >> 5) & 0x07E0) |
            ((color >> 3) & 0x001F));
        *reinterpret_cast<uint16_t *>(px) = c16;
    }
}

static void draw_char(uint32_t col, uint32_t row, char c) {
    const uint8_t *glyph;
    if (c >= FONT_FIRST && c <= FONT_LAST)
        glyph = font_bitmap[c - FONT_FIRST];
    else
        glyph = font_bitmap[0];

    uint32_t x0 = col * FONT_W;
    uint32_t y0 = row * FONT_H;

    for (uint32_t dy = 0; dy < FONT_H; dy++) {
        uint8_t bits = glyph[dy];
        for (uint32_t dx = 0; dx < FONT_W; dx++) {
            plot(x0 + dx, y0 + dy, (bits & 0x80) ? FG : BG);
            bits <<= 1;
        }
    }
}

static void scroll_up() {
    uint32_t row_bytes = con.pitch * FONT_H;
    memmove(con.fb, con.fb + row_bytes, row_bytes * (con.rows - 1));
    memset(con.fb + row_bytes * (con.rows - 1), 0, row_bytes);
}

static void newline() {
    con.cx = 0;
    con.cy++;
    if (con.cy >= con.rows) {
        scroll_up();
        con.cy = con.rows - 1;
    }
}

void fb_init(const FramebufferInfo &fb) {
    con.fb     = reinterpret_cast<uint8_t *>(static_cast<uintptr_t>(fb.addr));
    con.pitch  = fb.pitch;
    con.width  = fb.width;
    con.height = fb.height;
    con.bpp    = fb.bpp;
    con.cx     = 0;
    con.cy     = 0;
    con.cols   = fb.width / FONT_W;
    con.rows   = fb.height / FONT_H;
    con.ready  = true;

    memset(con.fb, 0, static_cast<size_t>(con.pitch) * con.height);
}

void fb_putchar(char c) {
    if (!con.ready) return;

    if (c == '\n') {
        newline();
        return;
    }
    if (c == '\r') {
        con.cx = 0;
        return;
    }
    if (c == '\t') {
        uint32_t stop = (con.cx + 8) & ~7u;
        while (con.cx < stop && con.cx < con.cols)
            draw_char(con.cx++, con.cy, ' ');
        if (con.cx >= con.cols)
            newline();
        return;
    }

    draw_char(con.cx, con.cy, c);
    con.cx++;
    if (con.cx >= con.cols)
        newline();
}

bool fb_available() {
    return con.ready;
}
